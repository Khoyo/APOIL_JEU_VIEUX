using UnityEngine;
using System.Collections;

public class CLevel
{
	CPlayer m_Player;
	CPlayer m_Player2;
	CPlayer m_Player3;
	
	//-------------------------------------------------------------------------------
	///
	//-------------------------------------------------------------------------------
	public CLevel()
	{
		m_Player = new CPlayer(true);
		m_Player2 =  new CPlayer();
		m_Player3 =  new CPlayer();
	}
	
	//-------------------------------------------------------------------------------
	///
	//-------------------------------------------------------------------------------
	public void Init()
	{	
		m_Player.Init();
	}
	
	//-------------------------------------------------------------------------------
	///
	//-------------------------------------------------------------------------------
	public void Reset()
	{
		m_Player.Reset();
	}
	
	//-------------------------------------------------------------------------------
	///
	//-------------------------------------------------------------------------------
	public void Process(float fDeltatime)
	{
		m_Player.Process(fDeltatime);
		
	}
	
	public CPlayer getPlayer()
	{
		return m_Player;
	}
}
